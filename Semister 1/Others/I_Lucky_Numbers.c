#include <stdio.h>
int main()
{
    int n;
    scanf("%d", &n);
    if(n >= 10 && n <=99){
        //একটি ২ সংখ্যার ডিজিট থেকে প্রথম সংখ্যা বের করতে হলে প্রথমে সেটা ১০ দারা ভাগ করতে হবে। এতে প্রথমটা বের হবে। এবং দিতৃয় সংখ্যা বের করার জন্য সেই সংখাটিকে ১০ দারা মোড করবো ফলে পরের টা বের হয়ে যাবে।
        int a = n / 10, b = n%10;
        if(a % b == 0 || b % a == 0){
            printf("YES");
        }
        else{
            printf("NO");
        }
    }
    return 0;
}

