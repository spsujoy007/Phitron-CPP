numbers = [10, 65, 88, 99, 77, 65, 25]

# 1 is starting index 7 is end index and 2 is optional step of indexing
print(numbers[1 : 7 : 1])

# reverse
print(numbers[7:0:-1])

print(numbers[2:]) #start from 2 to end
print(numbers[:3]) #start from 0 and end to 3
print(numbers[:]) # full 
print(numbers[:: -1]) # array reverse